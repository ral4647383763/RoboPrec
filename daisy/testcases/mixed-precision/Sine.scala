

import daisy.lang._
import Real._


object Sine {
  // f32
  def sine_32(x: Real): Real = {
    require(x > -1.57079632679 && x < 1.57079632679)
    x - (x*x*x)/6.0 + (x*x*x*x*x)/120.0 - (x*x*x*x*x*x*x)/5040.0
  } ensuring(res => res +/- 4e-7)

