
import daisy.lang._
import Real._


object RigidBody2 {
  // f32
  def rigidBody2_32(x1: Real, x2: Real, x3: Real): Real = {
    require(-15.0 <= x1 && x1 <= 15 && -15.0 <= x2 && x2 <= 15.0 &&
      -15.0 <= x3 && x3 <= 15)
    2*(x1*x2*x3) + (3*x3*x3) - x2*(x1*x2*x3) + (3*x3*x3) - x2
  } ensuring(res => res +/- 2e-2)

  // 0.5 f32
