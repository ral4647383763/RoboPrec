import daisy.lang._
import Real._
import daisy.lang.Vector._

object avg {

	def avg(x: Vector): Real = {
require(x >= -62.54 && x <= 15.02 && x.size(100)
	 && x.specV(Set(((1, 3),(-58.32, 8.96)), ((6, 9),(-60.71, -16.3)), ((18, 28),(-30.32, -7.48)),
((32, 33),(-48.26, -27.67)), ((34, 34),(-38.17, -7.31)), ((36, 43),(-56.38, -53.76)),
((53, 63),(-49.69, -47.75)), ((67, 71),(-21.84, -11.93)), ((72, 74),(-38.35, -26.27)),
((76, 86),(-6.28, 2.54))))
	)

        val n: Real = x.length()
        val z = x.fold(0.0)((acc: Real, i: Real) => acc + i)
        z / n
    }


}